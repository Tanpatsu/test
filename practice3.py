# 引き算の結果を引かれた変数に代入する
# 下記のコードのtargetというコメントの付いた行を演算子を1つしか使わない行に書き換えて、現状と同じことを実現してください
a = 1024
a = a - 256  # target
print(a)

answer_8 = 1024
answer_8 -= 256
